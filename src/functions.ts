/**
 *
 * @param a El primer número de la suma
 * @param b El segundo número de la suma
 * @returns La suma aritmética de a + b
 */
const suma = (a: number, b: number): number => a + b

/**
 * La función devuelveNumeroLetras devuelve el número de letras
 * de un nombre
 * @param nombre El nombre del cual queremos saber el número de letras
 * @returns El número de letras del nombre
 */
const devuelveNumeroLetras = (nombre: string): number => nombre.length

export { suma, devuelveNumeroLetras }
